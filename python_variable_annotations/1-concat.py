def concat(str1: str, str2: str) -> str:
    """
    Concatène deux chaînes de caractères et renvoie le résultat.

    Args:
        str1 (str): La première chaîne de caractères.
        str2 (str): La seconde chaîne de caractères.

    Returns:
        str: La chaîne résultante de la concaténation de str1 et str2.
    """
    # Implémentation de la fonction ici
    return str1 + str2
